document.querySelector('.dark').addEventListener('click', function () {
  document.querySelector('*').style.backgroundColor = '#121212';
  document.querySelector('p').style.fontcolor = 'rgb(255, 255, 255)';
  document.querySelector('button').style.backgroundColor = 'rgb(255, 255, 255)';
  document.querySelector('button').style.fontcolor = '#bbe1fa';
  document.querySelector('legend').style.fontcolor = 'rgb(255, 255, 255)';
});
